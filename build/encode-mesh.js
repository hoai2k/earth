#!/usr/bin/env node
/* Pack plates-mesh.json into a compact base64 blob embedded in a JS file.
 * Layout per plate: verts int16 (deg*50) ×2 | elev uint8 | rgb uint8 ×3 | idx uint16 ×3/tri */
const fs = require('fs');
const path = require('path');
const DATA = path.join(__dirname, '..', 'data');
const mesh = JSON.parse(fs.readFileSync(path.join(DATA, 'plates-mesh.json'), 'utf8'));
const SCALE = 50;

const ids = Object.keys(mesh.plates);
const meta = [];
const buffers = [];
for (const id of ids) {
  const p = mesh.plates[id];
  const vc = p.verts.length / 2, tc = p.idx.length / 3;
  const vb = Buffer.alloc(vc * 2 * 2);
  for (let i = 0; i < vc * 2; i++) vb.writeInt16LE(Math.max(-32768, Math.min(32767, Math.round(p.verts[i] * SCALE))), i * 2);
  const eb = Buffer.from((p.elev || new Array(vc).fill(0)).map(v => Math.max(0, Math.min(255, v | 0))));
  const cb = Buffer.from((p.cols || new Array(vc * 3).fill(128)).map(v => Math.max(0, Math.min(255, v | 0))));
  const ib = Buffer.alloc(tc * 3 * 2);
  for (let i = 0; i < tc * 3; i++) ib.writeUInt16LE(p.idx[i], i * 2);
  buffers.push(vb, eb, cb, ib);
  meta.push({ id, name: p.name, vc, tc });
}
const blob = Buffer.concat(buffers).toString('base64');
const packed = { scale: SCALE, plates: meta, data: blob };
const out = `/* auto-generated by build/encode-mesh.js — packed plate meshes */
window.MESH_PACKED=${JSON.stringify(packed)};
window.MESH=(function(){const P=window.MESH_PACKED;const raw=atob(P.data);const bytes=new Uint8Array(raw.length);for(let i=0;i<raw.length;i++)bytes[i]=raw.charCodeAt(i);const dv=new DataView(bytes.buffer);let off=0;const plates={};for(const m of P.plates){const verts=new Array(m.vc*2);for(let i=0;i<m.vc*2;i++){verts[i]=dv.getInt16(off,true)/P.scale;off+=2;}const elev=new Array(m.vc);for(let i=0;i<m.vc;i++){elev[i]=bytes[off++];}const cols=new Array(m.vc*3);for(let i=0;i<m.vc*3;i++){cols[i]=bytes[off++];}const idx=new Array(m.tc*3);for(let i=0;i<m.tc*3;i++){idx[i]=dv.getUint16(off,true);off+=2;}plates[m.id]={name:m.name,verts,elev,cols,idx};}return{plates};})();
`;
fs.writeFileSync(path.join(DATA, 'mesh-embed.js'), out);
console.log('wrote data/mesh-embed.js', (fs.statSync(path.join(DATA, 'mesh-embed.js')).size / 1024).toFixed(0), 'KB');
